        <h2>Lasterketa Herrikoia</h2>
        <p>Antzuolako 8 miliak mendi lasterketa 2013ko Otsailean ospatu zen lehen aldiz. Orduztik honaino nabarmendu daitekeen ezaugarrietako bat lasterketaren herrikoitasuna da. Herritar ugari korrika irteten den arren, atzetik dagoen lanean ere makina bat jendek parte hartzen du. Antolakuntza, araudi eta izen emateetan talde txiki bat ibili ohi den arren, lasterketa aurreko hilabeteetan bide garbitze lanetan giro politean elkartzen da jendea. Zer esanik ez egunean bertan lasterketako bidegurutze bakoitzean petoa jantzi eta egun batez bada ere antolakuntzako kide izaten den jende mordoxka. Lan guzti honek herritarren bultzada izateak eman dio izana lasterketari.</p><p>
